public class MultipleQuadratic
{
	public static void main(String[] args) {
		double x = 2.0;
		double value = (3 * x * x) - (8 * x) + 4;
		System.out.println("At X = " + x + " the value is " + value);
		x = 5.0;
		value = (3 * x * x) - (8 * x) + 4;
		System.out.println("At X = " + x + " the value is " + value);
		x = 7.0;
		value = (3 * x * x) - (8 * x) + 4;
		System.out.println("At X = " + x + " the value is " + value);
	}
}